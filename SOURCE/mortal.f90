!======================================================================!
SUBROUTINE mortal
!----------------------------------------------------------------------!
! Kill trees that do not deserve to live.
!----------------------------------------------------------------------!
USE CONTROL
USE TREE
!----------------------------------------------------------------------!
IMPLICIT NONE
!----------------------------------------------------------------------!
write (*,*) Afoliage (211),NIND_alive
J = NIND_alive
DO I = 1, NIND_alive
  KI = LIVING (I)
  IF (Afoliage (KI) <= 0.0) THEN
    LIVING (I:J-1) = LIVING (I+1:J)
    J = J - 1
    write (*,*) I,KI,J
  ENDIF
END DO
NIND_alive = J
!----------------------------------------------------------------------!
! It may be sensible to tidy up remaining dead tree locations.
!----------------------------------------------------------------------!
END SUBROUTINE mortal
!======================================================================!
